// This is the jQuery to make the page scroll
// All praise to jQuery!

jQuery(document).ready(function($) { $("#scrollBlogpost").click(function() {
	    $('html, body').animate({
	        scrollTop: $("#blogPost").offset().top
	    }, 1200);
	    return false;
	});
});

